#Understand Mathematical Operator in Python: +, -, *, /, **, //, %
x = 5
y = 17

#Addition: +
print(x+y) #output: 22

#Substraction: -
print(x-y) #output: -12
print(y-x)

#Multiplication: *
print(x*y) #output: 85

#Division: /
print(x/y) #output: 0.29411764705882354
print(y/x) #output: 3.4

#Power: **
print(x**y) #output: 762939453125
print(y**x) #output: 1419857

#Integer division: //
print(x//y) #output: 0
print(y//x) #output: 3

#Remainder Operator: %
print(x%y) #output: 2
print(y%x) #output: 2

