#Understanding str() function: Converting Other(int, float, bool) data types to str data type.

#Int to Str:
x = 10
print(type(x)) #output: <class 'int'>
y = str(x)
print(type(y)) #output: <class 'str'>

#Float to Str:
a = 20.4
print(type(a)) #output: <class 'float'>
b = str(a)
print(type(b)) #output: <class 'str'>

#Bool to Str:
c = True
print(type(c)) #output: <class 'bool'>
d = str(c)
print(type(d)) #output: <class 'str'>
#Note: Any data type can be converted to string using str() function.



